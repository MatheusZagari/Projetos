package org.example.Exercicios.src.Controle;

public class While {
    public static void main(String[] args) {
        int contador = 0;

        while (contador <= 20) {
            System.out.printf("i = %d\n", contador);
            contador += 1; //incremento da variável, é o que ta adicioandno o valor no contador
        }
    }
}
